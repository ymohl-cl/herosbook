CREATE SCHEMA herosbook_database;
CREATE TABLE herosbook_database.users {
	ID   INT				UNIQUE NOT NULL,
	ID_PUBLIC INT			UNIQUE NOT NULL,
	PSEUDO VARCHAR (20)		UNIQUE NOT NULL,
	PASSWORD VARCHAR(60)	NOT NULL,
	AGE INT					NOT NULL,
	SEX CHAR				NOT NULL,
	EMAIL VARCHAR(245)		UNIQUE NOT NULL,
	PRIMARY KEY (ID)
};
